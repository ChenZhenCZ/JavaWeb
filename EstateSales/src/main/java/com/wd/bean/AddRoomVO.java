package com.wd.bean;

public class AddRoomVO {

	private String  buildingId;
	private String  houseId;
	private String  buildingName;
	private String  houseNumber;
	private String  firstFloor;
	private String  lastFloor;
	
	public String getBuildingId() {
		return buildingId;
	}
	public void setBuildingId(String buildingId) {
		this.buildingId = buildingId;
	}
	public String getHouseId() {
		return houseId;
	}
	public void setHouseId(String houseId) {
		this.houseId = houseId;
	}
	public String getBuildingName() {
		return buildingName;
	}
	public void setBuildingName(String buildingName) {
		this.buildingName = buildingName;
	}
	public String getHouseNumber() {
		return houseNumber;
	}
	public void setHouseNumber(String houseNumber) {
		this.houseNumber = houseNumber;
	}
	public String getFirstFloor() {
		return firstFloor;
	}
	public void setFirstFloor(String firstFloor) {
		this.firstFloor = firstFloor;
	}
	public String getLastFloor() {
		return lastFloor;
	}
	public void setLastFloor(String lastFloor) {
		this.lastFloor = lastFloor;
	}
	
	
	
}
